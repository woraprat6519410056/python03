#การรับค่าข้อมูลทางแป้นพิมพ์ด้วย Python
mint = input('ป้อนชื่อ : ')
play = input('ป้อนปีเกิด ค.ศ. : ')
print('-----------------------')
#ฟังก์ฃั่นในการแปลงข้อมูลจากข้อความเป็นตัวเลข inr( ), float( )
#ฟังก์ฃั่นในการแปลงข้อมูลจากข้อความเป็นข้อความ str( )
print(f'ยินดีต้อนรับคุณ {mint} เกิดปี {play} อายุ {2023 - int(play) }')
print('ยินดีต้อนรับคุณ',mint,'เกิดปี',play,'อายุ',2023-int(play))
print('ยินดีต้อนรับคุณ '+mint+' เกิดปี '+play+' อายุ '+ str(2023-int(play)))
print('ยินดีต้อนรับคุณ {} เกิดปี {} อายุ {}'.format(mint,play,2023- int(play)))
print('ยินดีต้อนรับคุณ %s เกิดปี %s อายุ %d' %(mint,play,2023-int(play)))